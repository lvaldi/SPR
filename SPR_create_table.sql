CREATE TABLE STUDENT (
ID SERIAL NOT NULL,
Name VARCHAR(20) NOT NULL,
PRIMARY KEY (ID));

CREATE TABLE PARENT (
ID SERIAL NOT NULL,
Name VARCHAR(20) NOT NULL,
PRIMARY KEY (ID));

CREATE TABLE MENTOR (
ID SERIAL NOT NULL,
Name VARCHAR(20) NOT NULL,
PRIMARY KEY (ID));

CREATE TABLE TASK (
Title VARCHAR(250) NOT NULL,
Content VARCHAR(250),
DUE_DATE DATE NOT NULL,
PRIMARY KEY (Title));
